-- 1) CREATION DE LA BASE DE DONNEES
DROP DATABASE IF EXISTS compta;
CREATE DATABASE compta CHARACTER SET utf8mb4;
USE compta;

-- 2) CREATION DES TABLES

-- TABLE FOURNISSEUR
CREATE TABLE FOURNISSEUR (
    ID INT PRIMARY KEY,
    NOM VARCHAR(100) NOT NULL
);

-- TABLE ARTICLE
CREATE TABLE ARTICLE (
    ID INT PRIMARY KEY,
    REF VARCHAR(20) NOT NULL,
    DESIGNATION VARCHAR(255) NOT NULL,
    PRIX DECIMAL(10,2) NOT NULL,
    ID_FOU INT NOT NULL,
    FOREIGN KEY (ID_FOU) REFERENCES FOURNISSEUR(ID)
);

-- TABLE BON
CREATE TABLE BON (
    ID INT PRIMARY KEY,
    NUMERO VARCHAR(20) NOT NULL,
    DATE_CMDE DATE NOT NULL,
    DELAI INT NOT NULL,
    ID_FOU INT NOT NULL,
    FOREIGN KEY (ID_FOU) REFERENCES FOURNISSEUR(ID)
);

-- TABLE COMPO
CREATE TABLE COMPO (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    QTE INT NOT NULL,
    ID_ART INT NOT NULL,
    ID_BON INT NOT NULL,
    FOREIGN KEY (ID_ART) REFERENCES ARTICLE(ID),
    FOREIGN KEY (ID_BON) REFERENCES BON(ID)
);

